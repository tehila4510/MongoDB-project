print("\n--- שאלה 4: שמות בלבד, ציון בין 80 ל-95 ---");
printjson(
  db.students.find(
    { finalMark: { $gt: 80, $lt: 95 } },
    { name: 1, _id: 0 }
  ).toArray()
);


// ---- שאלה 5: תלמידות עם ציון פחות מ-70 והמקצוע הוא הנדסאים או אדריכלות ----
print("\n--- שאלה 5: ציון מתחת 70 ומקצוע הנדסאים/אדריכלות ---");
printjson(
  db.students.find({
    finalMark: { $lt: 70 },
    subject: { $in: ["הנדסאים", "אדריכלות"] }
  }).toArray()
);


// ---- שאלה 6: כל הציונים יחד עם השם, מודפסים לפי סדר עולה ----
print("\n--- שאלה 6: שם + ציון, מסודר בסדר עולה ---");
printjson(
  db.students.find(
    {},
    { name: 1, finalMark: 1, _id: 0 }
  ).sort({ finalMark: 1 }).toArray()
);


// ---- שאלה 7 + 8: עדכון - הוסיפי לכל התלמידות שקיבלו 50, תוספת של 6% ----
print("\n--- שאלה 8: הוספת 6% לתלמידות שקיבלו 50 ---");
printjson(
  db.students.updateMany(
    { finalMark: 50 },
    { $mul: { finalMark: 1.06 } }   // הכפלה ב-1.06 = תוספת של 6%
  )
);


// ---- שאלה 9: מחקי את התלמידות שלומדות הנדסאים וקיבלו פחות מ-50 בציון הסופי ----
print("\n--- שאלה 9: מחיקת תלמידות הנדסאים עם ציון מתחת ל-50 ---");
printjson(
  db.students.deleteMany({
    subject: "הנדסאים",
    finalMark: { $lt: 50 }
  })
);


// ---- שאלה 10: שאילתת חיפוש על השדה 'מקצוע' - כל התלמידות שלומדות הוראה ----
